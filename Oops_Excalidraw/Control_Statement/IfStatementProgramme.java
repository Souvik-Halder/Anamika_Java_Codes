
class IfStatementProgramme {
    public static void main(String[] args) {
        int x=10;
        if(x>10){
            System.out.println("X is greater than 10");
        }
        else if(x==10){
            System.out.println("X is equals to 10");
        }
        else{
            System.out.println("X is lesser than 10");
        }
    }
}
